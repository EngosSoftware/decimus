use super::*;
use decimus::{BidUint128, bid128_fma, bid128_from_string};

macro_rules! check {
  ($rnd_mode:expr, $x:expr, $y:expr, $z:expr, $expected:expr, $expected_flags:expr) => {
    let mut actual_flags: IdecFlags = 0;
    let expected = BidUint128 { w: [$expected[1], $expected[0]] };
    let x = BidUint128 { w: [$x[1], $x[0]] };
    let y = BidUint128 { w: [$y[1], $y[0]] };
    let z = BidUint128 { w: [$z[1], $z[0]] };
    let actual = bid128_fma(x, y, z, $rnd_mode, &mut actual_flags);
    assert_eq!(expected, actual);
    assert_eq!($expected_flags, actual_flags, "Result flags error, expected = 0x{:02X}, actual = 0x{:02X}", $expected_flags, actual_flags);
  };
  ($rnd_mode:expr, $x_rnd_mode:expr, $y_rnd_mode:expr, $z_rnd_mode:expr, $x:expr, $y:expr, $z:expr, $expected:expr, $expected_flags:expr, $x_flags:expr, $y_flags:expr, $z_flags:expr) => {
    let mut actual_flags: IdecFlags = 0;
    let expected = BidUint128 { w: [$expected[1], $expected[0]] };
    let x: BidUint128 = bid128_from_string($x, $x_rnd_mode, &mut actual_flags);
    assert_eq!($x_flags, actual_flags, "X flags error, expected = {:02X}, actual = {:02X}", $x_flags, actual_flags);
    let y: BidUint128 = bid128_from_string($y, $y_rnd_mode, &mut actual_flags);
    assert_eq!($y_flags, actual_flags, "Y flags error, expected = {:02X}, actual = {:02X}", $y_flags, actual_flags);
    let z: BidUint128 = bid128_from_string($z, $z_rnd_mode, &mut actual_flags);
    assert_eq!($z_flags, actual_flags, "Z flags error, expected = {:02X}, actual = {:02X}", $z_flags, actual_flags);
    let actual = bid128_fma(x, y, z, $rnd_mode, &mut actual_flags);
    assert_eq!(expected, actual);
    assert_eq!($expected_flags, actual_flags, "Result flags error, expected = 0x{:02X}, actual = 0x{:02X}", $expected_flags, actual_flags);
  };
}

